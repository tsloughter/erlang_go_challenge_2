-module(go2_app).

-behaviour(application).

%% Application callbacks
-export([start/2
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    Port = application:get_env(go2, port, 8080),
    {ok, _} = ranch:start_listener(go2, 100,
                                   ranch_tcp, [{port, Port}],
                                   go2, []),
    go2_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

